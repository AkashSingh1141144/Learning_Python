#  setting the specific data type

x = str('Sandhya')
print(x)

x = int(20)
print(x)

x = float(20.5)
print(x)

x = complex(5j)
print(x)

x = bool(True)
print(x)

x = list(("Sandhya", "Manshi", "Madhuri", "Pooja", "Priyanka", "Pallavi"))
print(x)

x = tuple(("Sandhya", "Manshi", "Madhuri", "Pooja", "Priyanka", "Pallavi"))
print(x)

x = set(("Sandhya", "Manshi", "Madhuri", "Pooja", "Priyanka", "Pallavi"))
print(x)

x = dict(Name="Sandhya", Age=20, City="Mirzapur", State="UP")
print(x)

x = frozenset(("Sandhya", "Manshi", "Madhuri", "Pooja", "Priyanka", "Pallavi"))
print(x)

x = bytes(5)
print(x)

x = bytearray(5)
print(x)